    [SerializeField] Toggle toggleMusic;

    [SerializeField] Button restartButton;
    [SerializeField] Button startButton;


    public float gameSpeed = 2.0f;
    int points = 0;
    public bool isGameOver;
    public bool isGameStarted;
    public string isSoundOn;


    // Start is called before the first frame update
    void Start()
    {

        DOTween.Init(true, true);

        isGameOver = true;
        isSoundOn = PlayerPrefs.GetString("isSoundOn", "on");
        toggleMusic.GetComponent<Toggle>().SetIsOnWithoutNotify(isSoundOn == "on" ? true : false);
       
        InitializePath(GameObject.Find("Pad1"), positions1);
        InitializePath(GameObject.Find("Pad2"), positions2);


   
    }

    void InitializePath( GameObject newPad, Vector3[,] positions )
    {
        int randomPath = UnityEngine.Random.Range(0, 250);

        for (int a = 0; a < 8; a++)
        {
            for (int b = 0; b < 4; b++)
            {


                //Debug.Log(paths[randomPath, a, b]);

                switch (paths[randomPath, a, b])
                {
                    case "N":
                        Instantiate(knight, positions[a, b], knight.transform.rotation).transform.SetParent(newPad.transform);
                        break;
                    case "P":
                        Instantiate(pawn, positions[a, b], transform.rotation).transform.SetParent(newPad.transform);
                        break;
                    case "p":
                        Instantiate(player, positions[a, b] - new Vector3(0, 0.2f, 0), transform.rotation).transform.SetParent(newPad.transform);
                        break;
                    case "R":
                        Instantiate(rook, positions[a, b], transform.rotation).transform.SetParent(newPad.transform);
                        break;
                    case "B":
                        Instantiate(bishop, positions[a, b], transform.rotation).transform.SetParent(newPad.transform);
                        break;
                    case "K":
                        Instantiate(king, positions[a, b], transform.rotation).transform.SetParent(newPad.transform);
                        break;

                }

            }

        }
    }

    public void startGame()
    {
        startButton.gameObject.SetActive(false);

        GameObject.Find("Camera").transform.DOMove(new Vector3(0.18f, 18.39f, -17.82f), 1.0f)
            .OnComplete(() =>
            {
                isGameOver = false;


                Invoke("IncreaseSpeed", 5.0f);
                Invoke("IncreaseSpeed", 10.0f);

            });

        GameObject.Find("Camera").transform.rotation.eulerAngles.Set(25.552f, 0f, 0f);
    }

    void IncreaseSpeed()
    {
        gameSpeed += 1.0f;
    }

   public void IncreasePoints()
    {
        points += 1;

        if (points < 10)
        {
            timeText.text = "Points: 00" + points;
        } 

        else if (points < 100){
            timeText.text = "Points: 0" + points;
        } 

        else
        {
            timeText.text = "Points: " + points;
        }


    }

    public void toggleSound()
    {
        if(toggleMusic.GetComponent<Toggle>().isOn)
        {
            PlayerPrefs.SetString("isSoundOn", "on");
            isSoundOn = "on";

        } else
        {
            PlayerPrefs.SetString("isSoundOn", "off");
            isSoundOn = "off";
        }
    }

    public void FinishGame()
    {
        isGameOver = true;
        Invoke("RestartGame", 2.0f);
    }


   public void RestartGame()
    {
        SceneManager.LoadScene(SceneManager.GetActiveScene().name);

    }

